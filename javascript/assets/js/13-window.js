/* -------------------- WINDOW -------------------- */

/*

    L'objet window en javascript est un objet global qui représente la fenetre d'un navigateur.
    Cet objet est crée automatiquement lorsque la page est chargée.

*/

// L'objet window est un objet important en Javascript car il fournit une interface entre le code Javascript et la fenetre du navigateur
console.log(window);

// La propriété 'window.console' permet d'accéder à l'objet console.
console.log(window.console);

// La propriété 'window.alert()' permet d'afficher une boite de dialogue.*
window.alert(`Hello, world !!!`)

// La propriété 'window.document' permet d'acceder à l'objet document
console.log(window.document);
